function rgbToHSL({ r, g, b }) {

    let max = Math.max(r, g, b);
    let min = Math.min(r, g, b);
    let h, s, l = (max + min) / 2;

    if (max === min) {
        h = s = 0;
    } else {
        let d = max - min;
        s = l > 0.5 ? d / (2 - max - min) : d / (max + min);
        switch (max) {
            case r: h = (g - b) / d + (g < b ? 6 : 0); break;
            case g: h = (b - r) / d + 2; break;
            case b: h = (r - g) / d + 4; break;
        }
        h /= 6;
    }

    return [h * 360, s * 100, l * 100];
}

function getComplementaryColor(rgb) {
    let [h, s, l] = rgb2hsl(rgb.r, rgb.g, rgb.b);
    h = (h + 180) % 360;
    return [h, s, l];
}

function parseHSL(hslString) {
    const matches = hslString.match(/hsla?\((\d+),\s*(\d+)%?,\s*(\d+)%?/i);
    return matches ? {
        h: parseInt(matches[1]),
        s: parseInt(matches[2]),
        l: parseInt(matches[3])
    } : null;
}

function createColorVariants(accentColor) {
    const base = parseHSL(accentColor);
    if (!base) return null;

    return {
        lighter: `hsl(${base.h}, ${Math.max(base.s - 5, 0)}%, ${Math.min(base.l + 15, 100)}%)`,
        light: `hsl(${base.h}, ${Math.max(base.s - 2, 0)}%, ${Math.min(base.l + 7, 100)}%)`,
        base: accentColor,
        dark: `hsl(${base.h}, ${Math.min(base.s + 2, 100)}%, ${Math.max(base.l - 7, 0)}%)`,
        darker: `hsl(${base.h}, ${Math.min(base.s + 5, 100)}%, ${Math.max(base.l - 15, 0)}%)`
    };
}

